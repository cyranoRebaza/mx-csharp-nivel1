using System;

namespace U05_EJ04
{
    class Program
    {
        static void Main(string[] args)
        {
            /* 
            4. Hacer un programa para mostrar los números del 10 al 1. No se debe realizar
            ningún pedido de datos.
            
             */

            for (int i = 10; i >= 1; i--)
            {
                Console.WriteLine(i);

            }

            Console.ReadKey();


        }
    }

}